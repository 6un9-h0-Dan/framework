namespace Spiral\Reactor\ClassElements;

/**
 * Class constant linked to some php expression of other constant.
 */
class PHPConstant
{
    /**
     * @var string
     */
    private $value = '';

    /**
     * @param string $value
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }
}